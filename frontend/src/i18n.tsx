(Object.keys(translationOverrides) as LanguageCode[]).forEach((lang) => {
  Object.assign(dictionaries[lang], translationOverrides[lang]);
});

type I18nContextValue = {
  lang: LanguageCode;
  setLang: (l: LanguageCode) => void;
  t: (key: string) => string;
};

const I18nContext = createContext<I18nContextValue | undefined>(undefined);

export const I18nProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [lang, setLang] = useState<LanguageCode>(() => {
    const stored = localStorage.getItem('lang');
    if (stored === 'hi' || stored === 'ta' || stored === 'en') return stored;
    return 'en';
  });

  useEffect(() => {
    localStorage.setItem('lang', lang);
  }, [lang]);

  const t = useMemo(() => {
    return (key: string) => {
      const dict = dictionaries[lang] || dictionaries.en;
      return dict[key] || dictionaries.en[key] || key;
    };
  }, [lang]);

  const value: I18nContextValue = { lang, setLang, t };
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};

export const useI18n = () => {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error('useI18n must be used within I18nProvider');
  return ctx;
};


